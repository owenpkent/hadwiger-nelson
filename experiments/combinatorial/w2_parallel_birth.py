from __future__ import annotations

import itertools
import json
import pathlib

K = 5
CACHE = pathlib.Path(__file__).resolve().parent / "_cache"
CACHE.mkdir(exist_ok=True)

TYPES = ["e", "s", "Oac", "Oad", "Obc", "Obd", "dis"]
OVERLAPS = {"Oac", "Oad", "Obc", "Obd"}
TRANSPORTERS = {"e", "s"}

X = [(a, b) for a in range(K) for b in range(K) if a != b]
IDX = {x: i for i, x in enumerate(X)}
N = len(X)


def type_of(x, y):
    (a, b), (c, d) = x, y
    ac, ad, bc, bd = a == c, a == d, b == c, b == d
    if ac and bd:
        return "e"
    if ad and bc:
        return "s"
    if ac:
        return "Oac"
    if ad:
        return "Oad"
    if bc:
        return "Obc"
    return "Obd" if bd else "dis"


ORB = {t: set() for t in TYPES}
for x in X:
    for y in X:
        ORB[type_of(x, y)].add((IDX[x], IDX[y]))

EQ_SETS = {  # orbital types in which slot pair (i,j) is EQUAL-colored
    ("L1", "R1"): {"Oac", "e"},
    ("L1", "R2"): {"Oad", "s"},
    ("L2", "R1"): {"Obc", "s"},
    ("L2", "R2"): {"Obd", "e"},
}


def rel_of(profile):
    R = set()
    for t in profile:
        R |= ORB[t]
    return frozenset(R)


def profile_of(R):
    return frozenset(t for t in TYPES if ORB[t] & R)


def is_stealth(profile):
    """All four cross pairs admit both = and != (invisible to width-1 sweeps)."""
    for eq in EQ_SETS.values():
        if not (set(profile) & eq):
            return False
        if not (set(profile) - eq):
            return False
    return True


def forcing_pairs(profile):
    """Cross pairs forced different (profile misses both eq types) or same."""
    forced_diff = [p for p, eq in EQ_SETS.items() if not (set(profile) & eq)]
    forced_same = [p for p, eq in EQ_SETS.items() if not (set(profile) - eq)]
    return forced_diff, forced_same


def compose(R, S):
    by_left = {}
    for (y, z) in S:
        by_left.setdefault(y, set()).add(z)
    return frozenset((x, z) for (x, y) in R for z in by_left.get(y, ()))


