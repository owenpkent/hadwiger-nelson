# --- Seed radius family: r -> (cos theta, sin theta, ring sqrt) ---

def spindle_angle_for_radius(r_squared: int) -> tuple[sp.Rational, sp.Expr, int]:
    """Given a squared seed radius r^2 (integer), return (cos theta, sin theta, k_sq).

    cos theta = 1 - 1/(2 r^2) = (2 r^2 - 1) / (2 r^2)
    sin theta = sqrt(1 - cos^2) = sqrt((4 r^2 - 1) / (4 r^4)) = sqrt(4 r^2 - 1) / (2 r^2)

    So sin theta lives in Q(sqrt(4 r^2 - 1)). The number 4 r^2 - 1 may or may not
    be a perfect square; if not, the field is a genuine quadratic extension.
    """
    rs = sp.Integer(r_squared)
    cos_t = (2 * rs - 1) / (2 * rs)
    discriminant = 4 * rs - 1
    sin_t = sp.sqrt(discriminant) / (2 * rs)
    return cos_t, sin_t, int(discriminant)


def moser_style_spindle(r_squared: int) -> tuple[list[tuple[sp.Expr, sp.Expr]], list[tuple[int, int]]]:
    """Generalized Moser spindle with seed radius sqrt(r_squared).

    Construction: two equilateral triangles share an edge to form a rhombus
    (4 vertices, 4 unit edges plus 1 unit short diagonal = K_4 minus an edge).
    Two copies of this rhombus share vertex A (the origin). The second copy is
    rotated by angle theta = arccos((2 r^2 - 1)/(2 r^2)) about A. The seed
    radius is chosen so that the outer tips D and D' (at distance sqrt(r_squared)
    from A in each copy) lie at unit distance from each other.

    For r^2 = 3 this is the original Moser spindle.

    Returns (vertices, unit_distance_edges) using exact sympy expressions.
    """
    sqrt_r2 = sp.sqrt(r_squared)
    sqrt3 = sp.sqrt(3)

    A = (sp.Integer(0), sp.Integer(0))

    # First rhombus: A, B, C, D with all unit edges except (A, D).
    # B is at (1, 0); C is at (1/2, sqrt 3 / 2) — both unit-distance from A and from each other.
    # D is at (3/2, sqrt 3 / 2), unit-distance from B and C; |AD| = sqrt(3) only when r_squared = 3.
    # For general r_squared we need a different rhombus where |AD| = sqrt(r_squared).
    #
    # Standard rhombus geometry: a rhombus with all 4 sides unit and short-diagonal unit gives
    # vertex distances |AB| = |AC| = |BD| = |CD| = 1 (sides), |BC| = 1 (short diag), |AD| = sqrt(3).
    # The vertex-pair distance |AD| is FIXED at sqrt(3) for the unit-edge unit-short-diagonal
    # rhombus geometry. This means r_squared = 3 is the ONLY value that gives the standard
    # Moser spindle's rhombus structure.

    # For other r_squared, the construction is different: the "spindle" must use a different
    # base structure. Here we generalize: skip the rhombus, build a simpler 3-vertex base
    # (an equilateral triangle around A), with D at radius sqrt(r_squared) and the spindle
    # arm pointing in a useful direction. The 5-vertex motif (A, B, C, D, D') with B, C, D in
    # the first arm and D' as the rotated tip cannot all be unit-edge generally.

    # Decision: only the r_squared = 3 case gives a true Moser-style spindle. For other r^2,
    # we fall back to a simpler "k-rhombus + rotated tip" structure that is NOT itself a
    # 4-chromatic UDG. Document but do not return spurious structures.

    if r_squared != 3:
        raise NotImplementedError(
            f"True Moser-style spindle requires r^2 = 3 (Moser standard). "
            f"For r^2 = {r_squared}, the rhombus geometry doesn't close up with unit edges. "
            f"Use rotation_orbit_graph(seed_vertex, theta, copies) instead for a "
            f"k-rotated orbit experiment."
        )

    sqrt11 = sp.sqrt(11)
    # cos theta = 5/6, sin theta = sqrt(11)/6
    cos_t = sp.Rational(5, 6)
    sin_t = sqrt11 / 6

    A = (sp.Integer(0), sp.Integer(0))
    B = (sp.Integer(1), sp.Integer(0))
    C = (sp.Rational(1, 2), sqrt3 / 2)
    D = (sp.Rational(3, 2), sqrt3 / 2)

    def rot(p):
        return (cos_t * p[0] - sin_t * p[1], sin_t * p[0] + cos_t * p[1])

    Bp = rot(B)
    Cp = rot(C)
    Dp = rot(D)
    verts = [A, B, C, D, Bp, Cp, Dp]

    edges = []
    N = len(verts)
    for i in range(N):
        for j in range(i + 1, N):
            dx = verts[i][0] - verts[j][0]
            dy = verts[i][1] - verts[j][1]
            d2 = sp.simplify(dx * dx + dy * dy)
            if d2 == 1:
                edges.append((i, j))
    return verts, edges


def rotation_orbit_graph(
    seed_vertices: list[tuple[sp.Expr, sp.Expr]],
    cos_t: sp.Expr,
    sin_t: sp.Expr,
    n_rotations: int,
) -> tuple[list[tuple[sp.Expr, sp.Expr]], list[tuple[int, int]]]:
    """Apply n_rotations applications of the rotation (cos_t, sin_t) to each seed vertex,
    dedup, and compute all unit-distance edges.

    Used to build orbit graphs from non-Moser rotation angles. For instance, take
    a unit triangle and apply order-7 rotations to get 21 vertices.
    """
    all_verts = []
    seen = set()
    for k in range(n_rotations):
        # Rotation k * theta. Use Chebyshev: cos(k theta), sin(k theta).
        ct, st = sp.Integer(1), sp.Integer(0)
        for _ in range(k):
            ct, st = cos_t * ct - sin_t * st, sin_t * ct + cos_t * st
        ct = sp.simplify(ct)
        st = sp.simplify(st)
        for (x, y) in seed_vertices:
            xr = sp.simplify(ct * x - st * y)
            yr = sp.simplify(st * x + ct * y)
            key = (xr, yr)
            if key not in seen:
                seen.add(key)
                all_verts.append((xr, yr))

    edges = []
    N = len(all_verts)
    for i in range(N):
        for j in range(i + 1, N):
            dx = all_verts[i][0] - all_verts[j][0]
            dy = all_verts[i][1] - all_verts[j][1]
            d2 = sp.simplify(dx * dx + dy * dy)
            if d2 == 1:
                edges.append((i, j))
    return all_verts, edges


def sat_chromatic_check(N: int, edges: list[tuple[int, int]], k: int) -> tuple[bool, list[int] | None, float]:
    """SAT-check if the graph (N vertices, edges) is k-colorable.

    Encoding: variables x_{v, c} for v in [N], c in [k]. x_{v,c} = true means vertex v has color c.
    Constraints: each vertex has at least one color; no two adjacent vertices have the same color.

    Returns (is_sat, coloring, time). Note: "is_sat" means "k-colorable" here.
    """
    def var(v, c):
        return v * k + c + 1   # 1-indexed

    clauses = []
    for v in range(N):
        clauses.append([var(v, c) for c in range(k)])    # at least one color
        for c1 in range(k):
            for c2 in range(c1 + 1, k):
                clauses.append([-var(v, c1), -var(v, c2)])    # at most one color
    for (u, v) in edges:
        for c in range(k):
            clauses.append([-var(u, c), -var(v, c)])    # no monochromatic edge

    t0 = time.time()
    with Cadical195(bootstrap_with=clauses) as solver:
        sat = solver.solve()
        model = solver.get_model() if sat else None
    elapsed = time.time() - t0

    coloring = None
    if sat and model is not None:
        coloring = [-1] * N
        for v in range(N):
            for c in range(k):
                if var(v, c) in model:
                    coloring[v] = c
                    break
    return sat, coloring, elapsed


